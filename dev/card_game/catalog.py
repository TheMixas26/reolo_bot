def get_rarity_label(rarity_code: str | None) -> str:
    if not rarity_code:
        return "?"
    return rarity_code.split("-", 1)[-1]


def get_rarity_rank(rarity_code: str | None) -> int:
    if not rarity_code:
        return 99
    prefix = rarity_code.split("-", 1)[0]
    try:
        return int(prefix)
    except ValueError:
        return RARITY_LABEL_RANKS.get(get_rarity_label(rarity_code), 99)


def card_sort_key(card: dict) -> tuple[int, int, str]:
    return (
        get_rarity_rank(card.get("rarity")),
        int(card.get("id", 0)),
        str(card.get("name", "")),
    )


def sort_cards(cards: list[dict]) -> list[dict]:
    return sorted(cards, key=card_sort_key)


def get_pack_name(card: dict) -> str:
    return str(card.get("category") or "Безымянный пак")
